let age = 6;

if (age < 3) {
  console.log("Too young for school");
} else if (age >= 3 && age < 5) {
  console.log("Go to preschool");
} else if (age >= 5 && age < 11) {
  console.log("Go to elementary school");
} else if (age >= 11 && age < 14) {
  console.log("Go to middle school");
} else if (age >= 14 && age < 18) {
  console.log("Go to high school");
} else {
  console.log("Go to college or work");
}
